package com.leetcode.himanshu.codes;

import java.util.Scanner;

public class Spoj {
    public static void main (String[] args) throws java.lang.Exception
    {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while(t != 42){
            System.out.println(t);
            t = sc.nextInt();
        }
    }
}
